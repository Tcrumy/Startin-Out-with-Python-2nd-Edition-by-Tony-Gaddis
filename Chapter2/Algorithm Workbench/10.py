# Assume the variable sales references a float value. Write a statement that 
# displays the value rounded to two decimal points.

# NOTE: We will first give the sales variable a float value. This is required
# for the program to run. This value can be any float number. I chose 173.6
sales = 173.6

# This will display the sales variable to 2 decimal places
print(format(sales, ".2f"))
